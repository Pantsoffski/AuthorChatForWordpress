package pl.ordin.authorchatforwordpress;

/**
 * {@link CustomArrayList} this is my custom array list object.
 */
public class CustomArrayList {
    public String nick;
    public String chat;
    public String date;

    public CustomArrayList(String n, String c, String d) {
        this.nick = n;
        this.chat = c;
        this.date = d;
    }
}

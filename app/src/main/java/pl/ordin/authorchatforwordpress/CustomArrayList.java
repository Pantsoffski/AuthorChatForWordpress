package pl.ordin.authorchatforwordpress;

/**
 * {@link CustomArrayList} custom array list object.
 */
class CustomArrayList {
    String nick;
    String chat;
    String date;

    CustomArrayList(String n, String c, String d) {
        this.nick = n;
        this.chat = c;
        this.date = d;
    }
}
